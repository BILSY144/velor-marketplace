// Native-language outreach copy for the founding-seller campaign.
//
// Why this exists: Velor is recruiting sellers worldwide, weighted towards the
// East. A cold email that opens in the seller's own language is the single
// strongest signal that a global marketplace actually means it. This is a
// deliberate selling point, not a nicety -- the emails also promise that the
// seller can deal with Velor in their own language, which is TRUE because the
// AI assistant and the agent inbox answer in whatever language they write in.
//
// Zero runtime cost: these are hand-written strings, resolved at send time
// from the prospect's country. No translation API, no per-email charge.
// English is the fallback for any country not mapped below.

export type OutreachLang =
  | 'en' | 'es' | 'fr' | 'de' | 'it' | 'pt' | 'nl' | 'pl' | 'tr' | 'ru'
  | 'ar' | 'hi' | 'bn' | 'vi' | 'th' | 'id' | 'zh' | 'ja' | 'ko'

export const RTL_OUTREACH_LANGS: OutreachLang[] = ['ar']

// Country (ISO-2, or the loose labels our scout sources emit) -> language.
// Anything absent stays English on purpose: we would rather send good English
// than bad machine-translated copy.
const COUNTRY_LANG: Record<string, OutreachLang> = {
  ES: 'es', MX: 'es', AR: 'es', CO: 'es', CL: 'es', PE: 'es',
  FR: 'fr', BE: 'fr',
  DE: 'de', AT: 'de', CH: 'de',
  IT: 'it',
  PT: 'pt', BR: 'pt',
  NL: 'nl',
  PL: 'pl',
  TR: 'tr',
  RU: 'ru', BY: 'ru', KZ: 'ru',
  AE: 'ar', SA: 'ar', EG: 'ar', MA: 'ar', JO: 'ar', QA: 'ar', KW: 'ar',
  IN: 'hi',
  BD: 'bn',
  VN: 'vi',
  TH: 'th',
  ID: 'id', MY: 'id',
  CN: 'zh', TW: 'zh', HK: 'zh', SG: 'zh',
  JP: 'ja',
  KR: 'ko',
}

export function langForCountry(country: string | null | undefined): OutreachLang {
  if (!country) return 'en'
  const key = country.trim().toUpperCase()
  if (COUNTRY_LANG[key]) return COUNTRY_LANG[key]
  // Scout sources sometimes emit free-text locations ("Bali, Indonesia").
  const upper = key
  const byName: Array<[string, OutreachLang]> = [
    ['SPAIN', 'es'], ['MEXICO', 'es'], ['FRANCE', 'fr'], ['GERMANY', 'de'],
    ['ITALY', 'it'], ['PORTUGAL', 'pt'], ['BRAZIL', 'pt'], ['NETHERLANDS', 'nl'],
    ['POLAND', 'pl'], ['TURKEY', 'tr'], ['TURKIYE', 'tr'], ['RUSSIA', 'ru'],
    ['EMIRATES', 'ar'], ['SAUDI', 'ar'], ['EGYPT', 'ar'], ['MOROCCO', 'ar'],
    ['INDIA', 'hi'], ['BANGLADESH', 'bn'], ['VIETNAM', 'vi'], ['THAILAND', 'th'],
    ['INDONESIA', 'id'], ['MALAYSIA', 'id'], ['CHINA', 'zh'], ['TAIWAN', 'zh'],
    ['HONG KONG', 'zh'], ['SINGAPORE', 'zh'], ['JAPAN', 'ja'], ['KOREA', 'ko'],
  ]
  for (const [name, lang] of byName) {
    if (upper.includes(name)) return lang
  }
  return 'en'
}

